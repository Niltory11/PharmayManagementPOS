    private void txtPercentageKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPercentageKeyReleased
        // TODO add your handling code here:

        String percentage = txtPercentage.getText(); // Assuming you have a JTextField named txtPercentage
        String price = txtPricePerUnit.getText();
        String quantity = txtQuantity.getText();

        try {
            int qty = Integer.parseInt(quantity);
            int unitPrice = Integer.parseInt(price);
            int totalPrice = qty * unitPrice;

            if (!percentage.isEmpty()) {
                double discount = Double.parseDouble(percentage) / 100.0;
                totalPrice = (int) (totalPrice * (1 - discount)); // Applying the discount percentage
            }

            txtTotal.setText(String.valueOf(totalPrice));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid numbers for Quantity, Price, and Percentage!");
        }


    }//GEN-LAST:event_txtPercentageKeyReleased

