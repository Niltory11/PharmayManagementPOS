    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int checkMedicineExist = 0;
        String batch = txtBatch.getText().trim();

        if (batch.equals("")) {
            JOptionPane.showMessageDialog(null, "Batch name is required!");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                String query = "SELECT * FROM medicine WHERE  batch = ?";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, batch);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    txtName.setText(rs.getString("name"));
                    txtExp.setText(rs.getString("expDate"));
                    txtComName.setText(rs.getString("companyName"));
                    txtPricePerUnit.setText(rs.getString("price"));
                    txtAvailableQty.setText(rs.getString("quantity"));
                    //  txtQuantity.setText(rs.getString("quantity"));

                    txtName.setEditable(true);
                    txtQuantity.setEditable(true);
                    checkMedicineExist = 1;
                }

                if (checkMedicineExist == 0) {
                    JOptionPane.showMessageDialog(null, "Medicine does not exist!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String avalableQty = txtAvailableQty.getText();
        String batch = txtBatch.getText().trim();
        String quantity = txtQuantity.getText();

        // String quantity = txtQuantity.getText();
        int totalQuantity = 0;
        if (quantity.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Current quantity is missing!");
            return;
        }

        try {
            int currentQuantity = Integer.parseInt(quantity);
            if (quantity.isEmpty()) {
                totalQuantity = currentQuantity;
            } else {
                totalQuantity = currentQuantity + Integer.parseInt(avalableQty);
            }

            Connection con = ConnectionProvider.getCon();
            String query = "UPDATE medicine SET  quantity = ? WHERE batch = ?";
            PreparedStatement pst = con.prepareStatement(query);

            pst.setInt(1, totalQuantity);
            pst.setString(2, batch);

            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Medicine Updated Successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Medicine not found!");
            }

            setVisible(false);
            new SalesReturn().setVisible(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Quantity must be a valid number!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

